package com.kk_electronic.kkportal.core.util;

import com.google.gwt.dom.client.DivElement;
import com.google.gwt.dom.client.Element;
import com.google.gwt.dom.client.Style.Unit;
import com.google.gwt.user.client.DOM;

public class EmCalculator {
	public static double getEmSize(Element e) {
		DivElement d = DOM.createDiv().cast();
		d.getStyle().setHeight(10.0, Unit.EM);
		e.appendChild(d);
		double pixelsize = e.getOffsetHeight();
		e.removeChild(d);
		return 10.0/pixelsize;
	}
}
